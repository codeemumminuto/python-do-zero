# Exercícios para praticar o for

'''
2. Faça um programa que imprima apenas os números
pares de 2 até 20 (dica: use 3 parâmetros)
'''

print("Exercício 2!!")
for i in range(2,21,2):
    print(i)

'''
3. Faça um programa que imprima os números de 20
até 0, utilizando o for(dica: use 3 parâmetros e
o terceiro será o número -1)
'''

print("Exercício 3!!")
for i in range(20,-1,-1):
    print(i)

'''
4. Faça um programa que imprima a tabuada de 3 na
tela utilizando o for;
'''

print("Exercício 4!!")
for i in range(10):
    print(f"{i} x 3 = {i*3}")